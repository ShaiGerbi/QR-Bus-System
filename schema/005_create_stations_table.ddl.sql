DROP TABLE IF EXISTS stations;

CREATE TABLE stations(
  station_id INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
  station_name VARCHAR(32) NOT NULL,
  station_code VARCHAR(16) UNIQUE NOT NULL,
  address VARCHAR(64),
  city VARCHAR(32),
  latitude DECIMAL(8, 6) NOT NULL CHECK(latitude BETWEEN -90.000000 AND 90.000000),
  longitude DECIMAL(9, 6) NOT NULL CHECK(longitude BETWEEN -180.000000 AND 180.000000),
  is_active BOOLEAN NOT NULL DEFAULT TRUE
);